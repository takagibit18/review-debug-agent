"""LLM-assisted fixture annotation."""

from __future__ import annotations

import json
from typing import Any

from pydantic import ValidationError

from eval.schemas import ExpectedIssue, ExpectedResult
from src.config import get_settings
from src.models.client import ModelClient
from src.models.schemas import Message, ModelConfig

ANNOTATION_TOOL_NAME = "submit_annotation"
ANNOTATION_CRITIQUE_TOOL_NAME = "submit_annotation_critique"

ANNOTATOR_SYSTEM_PROMPT = """You are creating golden-set annotations for code-review evaluation.
Return only issues that are directly supported by the diff and file context.
Do not invent defects, and do not report claims that conflict with visible code.
Valid issues: logic bugs, reliability regressions, security vulnerabilities, broken edge-case handling.
Invalid issues: normal feature additions, broad "might be risky" speculation, style preferences,
commented-out code cleanliness, and generic maintainability suggestions without concrete breakage.
Each issue must include concise evidence hints (file path + changed symbol or changed line behavior).
If a PR is mostly dependency/CI maintenance and no reliable defect exists, return an empty list.
"""

ANNOTATOR_CRITIQUE_PROMPT = """You are validating draft golden-set issues for code-review evaluation.
For each draft issue, decide whether it is a falsifiable defect claim grounded in changed code.
Keep only issues with concrete, testable breakage/risk evidence in the diff.
Drop issues that are feature commentary, style feedback, or speculative concerns.
"""

# OpenAI-compatible providers (e.g. DashScope) often cap total input around 129024 units;
# keep the serialized user JSON below that with headroom for system prompt + tool schema.
_MAX_ANNOTATION_USER_JSON_CHARS = 90_000
_DIFF_TRUNCATION_NOTE = "\n\n[truncated: diff_text exceeded model input budget]"
_MAX_CRITIQUE_DIFF_CHARS = 40_000


def build_annotation_user_json(
    *,
    repo_full_name: str,
    pr_number: int,
    pr_title: str,
    pr_body: str,
    diff_text: str,
    instructions: str,
    max_json_chars: int = _MAX_ANNOTATION_USER_JSON_CHARS,
) -> str:
    """Serialize the annotator user payload, truncating diff/body to stay under provider limits."""

    def payload(diff: str, body: str) -> dict[str, object]:
        return {
            "repo_full_name": repo_full_name,
            "pr_number": pr_number,
            "pr_title": pr_title,
            "pr_body": body,
            "diff_text": diff,
            "instructions": instructions,
        }

    d = diff_text or ""
    b = pr_body or ""

    def json_len(diff: str, body: str) -> int:
        return len(json.dumps(payload(diff, body), ensure_ascii=True))

    # Shrink oversized PR body first (templates can be huge).
    while len(b) > 8_000 and json_len(d, b) > max_json_chars:
        b = b[: max(0, int(len(b) * 0.85))]

    if json_len(d, b) <= max_json_chars:
        return json.dumps(payload(d, b), ensure_ascii=True)

    # Binary search longest diff prefix that fits (with truncation note when clipped).
    lo, hi = 0, len(d)
    best = 0
    while lo <= hi:
        mid = (lo + hi) // 2
        diff_part = d if mid >= len(d) else d[:mid] + _DIFF_TRUNCATION_NOTE
        if json_len(diff_part, b) <= max_json_chars:
            best = mid
            lo = mid + 1
        else:
            hi = mid - 1

    if best >= len(d):
        return json.dumps(payload(d, b), ensure_ascii=True)

    clipped = d[:best] + _DIFF_TRUNCATION_NOTE
    while json_len(clipped, b) > max_json_chars:
        if len(b) > 200:
            b = b[: max(0, len(b) // 2)]
            continue
        if best > 0:
            best = max(0, best // 2)
            clipped = d[:best] + _DIFF_TRUNCATION_NOTE
            continue
        clipped = _DIFF_TRUNCATION_NOTE
        break

    return json.dumps(payload(clipped, b), ensure_ascii=True)


class LLMAnnotator:
    """Generate ExpectedIssue drafts from PR metadata and diff."""

    def __init__(self, model_client: ModelClient | None = None) -> None:
        self._model_client = model_client or ModelClient()

    async def close(self) -> None:
        """Close model client resources."""
        await self._model_client.close()

    async def annotate(
        self,
        *,
        repo_full_name: str,
        pr_number: int,
        pr_title: str,
        pr_body: str,
        diff_text: str,
        max_issues: int = 5,
    ) -> ExpectedResult:
        """Return draft expected issues generated by LLM."""
        tool_schema = self._annotation_tool_schema()

        instructions = (
            "Return expected review issues supported by the provided diff and metadata. "
            "Cross-check each issue with concrete changed code facts before output. "
            "If no reliable issue is present, return an empty issues list. "
            "Do not output feature-only observations or style-only comments."
        )
        user_content = build_annotation_user_json(
            repo_full_name=repo_full_name,
            pr_number=pr_number,
            pr_title=pr_title,
            pr_body=pr_body,
            diff_text=diff_text,
            instructions=instructions,
        )
        messages = [
            Message(role="system", content=ANNOTATOR_SYSTEM_PROMPT),
            Message(role="user", content=user_content),
        ]
        response = await self._model_client.chat(
            messages=messages,
            config=ModelConfig(model=get_settings().model_name, temperature=0.0),
            tools=tool_schema,
        )
        parsed = self._parse_annotation(response.tool_calls, response.content)
        issues = parsed[: max(0, max_issues)]
        issues = await self._critique_issues(
            repo_full_name=repo_full_name,
            pr_number=pr_number,
            pr_title=pr_title,
            diff_text=diff_text,
            draft_issues=issues,
        )
        return ExpectedResult(
            issues=issues,
            min_issues=0,
            max_issues=max_issues,
            is_empty_annotation=len(issues) == 0,
        )

    async def _critique_issues(
        self,
        *,
        repo_full_name: str,
        pr_number: int,
        pr_title: str,
        diff_text: str,
        draft_issues: list[ExpectedIssue],
    ) -> list[ExpectedIssue]:
        if not draft_issues:
            return []

        critique_payload = {
            "repo_full_name": repo_full_name,
            "pr_number": pr_number,
            "pr_title": pr_title,
            "diff_text": (diff_text or "")[:_MAX_CRITIQUE_DIFF_CHARS],
            "draft_issues": [issue.model_dump(mode="json") for issue in draft_issues],
            "instructions": "Mark each issue with keep/drop. Keep only falsifiable defect claims.",
        }
        response = await self._model_client.chat(
            messages=[
                Message(role="system", content=ANNOTATOR_CRITIQUE_PROMPT),
                Message(role="user", content=json.dumps(critique_payload, ensure_ascii=True)),
            ],
            config=ModelConfig(model=get_settings().model_name, temperature=0.0),
            tools=self._critique_tool_schema(),
        )
        keep_indices = self._parse_critique_keep_indices(response.tool_calls, response.content)
        if keep_indices is None:
            # Fail open when critique parsing fails to avoid dropping all annotations accidentally.
            return draft_issues
        kept = [issue for idx, issue in enumerate(draft_issues) if idx in keep_indices]
        return kept

    @staticmethod
    def _annotation_tool_schema() -> list[dict[str, Any]]:
        return [
            {
                "type": "function",
                "function": {
                    "name": ANNOTATION_TOOL_NAME,
                    "description": "Submit structured expected issue annotations",
                    "parameters": {
                        "type": "object",
                        "properties": {
                            "issues": {
                                "type": "array",
                                "items": {
                                    "type": "object",
                                    "properties": {
                                        "severity": {
                                            "type": "string",
                                            "enum": ["critical", "warning", "info", "style"],
                                        },
                                        "location_pattern": {"type": "string"},
                                        "category": {"type": "string"},
                                        "description": {"type": "string"},
                                    },
                                    "required": [
                                        "severity",
                                        "location_pattern",
                                        "category",
                                        "description",
                                    ],
                                    "additionalProperties": False,
                                },
                            }
                        },
                        "required": ["issues"],
                        "additionalProperties": False,
                    },
                },
            }
        ]

    @staticmethod
    def _critique_tool_schema() -> list[dict[str, Any]]:
        return [
            {
                "type": "function",
                "function": {
                    "name": ANNOTATION_CRITIQUE_TOOL_NAME,
                    "description": "Submit keep or drop decision for each draft issue by index.",
                    "parameters": {
                        "type": "object",
                        "properties": {
                            "decisions": {
                                "type": "array",
                                "items": {
                                    "type": "object",
                                    "properties": {
                                        "index": {"type": "integer", "minimum": 0},
                                        "action": {"type": "string", "enum": ["keep", "drop"]},
                                        "reason": {"type": "string"},
                                    },
                                    "required": ["index", "action"],
                                    "additionalProperties": False,
                                },
                            }
                        },
                        "required": ["decisions"],
                        "additionalProperties": False,
                    },
                },
            }
        ]

    @staticmethod
    def _parse_annotation(
        tool_calls: list[dict[str, Any]],
        content: str,
    ) -> list[ExpectedIssue]:
        for call in tool_calls:
            function_data = call.get("function")
            if not isinstance(function_data, dict):
                continue
            if function_data.get("name") != ANNOTATION_TOOL_NAME:
                continue
            arguments = function_data.get("arguments", "{}")
            try:
                payload = json.loads(arguments)
            except json.JSONDecodeError:
                continue
            parsed = LLMAnnotator._issues_from_payload(payload)
            if parsed:
                return parsed

        # Fallback: parse raw JSON from content if tool call failed.
        try:
            payload = json.loads(content)
        except json.JSONDecodeError:
            return []
        return LLMAnnotator._issues_from_payload(payload)

    @staticmethod
    def _issues_from_payload(payload: Any) -> list[ExpectedIssue]:
        if not isinstance(payload, dict):
            return []
        issues = payload.get("issues", [])
        if not isinstance(issues, list):
            return []
        parsed: list[ExpectedIssue] = []
        for item in issues:
            if not isinstance(item, dict):
                continue
            try:
                parsed.append(ExpectedIssue.model_validate(item))
            except ValidationError:
                continue
        return parsed

    @staticmethod
    def _parse_critique_keep_indices(
        tool_calls: list[dict[str, Any]], content: str
    ) -> set[int] | None:
        payload: dict[str, Any] | None = None
        for call in tool_calls:
            function_data = call.get("function")
            if not isinstance(function_data, dict):
                continue
            if function_data.get("name") != ANNOTATION_CRITIQUE_TOOL_NAME:
                continue
            arguments = function_data.get("arguments", "{}")
            try:
                raw = json.loads(arguments)
            except json.JSONDecodeError:
                continue
            if isinstance(raw, dict):
                payload = raw
                break
        if payload is None:
            try:
                raw = json.loads(content)
            except json.JSONDecodeError:
                return None
            if isinstance(raw, dict):
                payload = raw
        if payload is None:
            return None

        decisions = payload.get("decisions", [])
        if not isinstance(decisions, list):
            return None
        keep_indices: set[int] = set()
        for decision in decisions:
            if not isinstance(decision, dict):
                continue
            idx = decision.get("index")
            action = str(decision.get("action", "")).lower()
            if isinstance(idx, int) and idx >= 0 and action == "keep":
                keep_indices.add(idx)
        return keep_indices

